#include <stdio.h>
#include <SDL2/SDL.h>

// -------------------------------------------------------------------------
// ;global
// -------------------------------------------------------------------------
SDL_Window *window = NULL;
SDL_Renderer *renderer = NULL;

// -------------------------------------------------------------------------
// ;init
// -------------------------------------------------------------------------
void sdl_init()
{
	SDL_Init(SDL_INIT_EVERYTHING);
	window = SDL_CreateWindow("Ultima", 0, 0, 1280, 720, 0);
	renderer = SDL_CreateRenderer(window, -1, 0);
}

// -------------------------------------------------------------------------
// ;main
// -------------------------------------------------------------------------
int main()
{
	sdl_init();

	SDL_Delay(1000);

	return 0;
}
